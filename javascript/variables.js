// --- Variável ---
// > É um espaço alocado na memória do computador para armazenar dados, que podemos
//   utilizar agora ou no futuro.

// > Palavras reservadas para declarar uma variável:
//    - var;
//    - let;
//    - const;

// > Convenções Javacsript
//    - camelCase para variáveis, constantes, funções e métodos;
//    - PascalCase para classes.

// > Obs.: a convenção de nomeclatura Kebab Case é utilizada em arquivos CSS ou,
//         em class HTML.

var variableOne = "camelCase";
let VariableTwo = "PascalCase";
const variable_three = "snake_case";
const VARIABLE_FOUR = "SCREAMING_SNAKE_CASE";

console.log(variableOne);
console.log(VariableTwo);
console.log(variable_three);
console.log(VARIABLE_FOUR);
